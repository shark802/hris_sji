<script>
$(function(){
  $('.in').click(function(e){
    e.preventDefault();
    var id = $(this).data('id');
    var status = "in";
    getRow(id, status);
  });

  $('.out').click(function(e){
    e.preventDefault();
    var id = $(this).data('id');
    var status = "out";
    getRow(id, status);
  });

  var interval = setInterval(function() {
    var momentNow = moment();
    $('#date').html(momentNow.format('dddd').substring(0,3).toUpperCase() + ' - ' + momentNow.format('MMMM DD, YYYY'));  
    $('#time').html(momentNow.format('hh:mm:ss A'));
  }, 100);
});

function getRow(id, status){
  $.ajax({
    type: 'POST',
    url: 'attendance_clock.php',
    data: {id:id,
          status:status
          },
    dataType: 'json',
    success: function(response){
        if(response.error){
          $('.alert').hide();
          $('.alert-danger').show();
          $('.message').html(response.message);
          refreshTable();
        }
        else{
          $('.alert').hide();
          $('.alert-success').show();
          $('.message').html(response.message);
          refreshTable();
        }
      }
  });
}


